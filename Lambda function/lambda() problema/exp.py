#Square of given number
sq=lambda a:a**2
print("Square of the given number = ",sq(2))

#Addition of two number
add=lambda a,b:a+b
print("Addition of two Number = ",add(5,2))

#Subtraction of two number
sub=lambda a,b:a-b
print("Subtraction of two Number = ",sub(5,2))

#Multiplication of two number
mul=lambda a,b:a*b
print("Multiplication of two Number = ",mul(5,2))


#Division of two number
div=lambda a,b:a/b
print("Division of two Number = ",div(5,2))

#Even number or not
check=lambda num:"Even" if num%2==0 else "Odd"
print(check(2))

